function LoginForm() {
  const searchParams = useSearchParams();
  const [isLoading, setIsLoading] = useState(false);

  useEffect(() => {
    const error = searchParams.get("error");
    if (error) {
      toast({
        type: "error",
        description: "Access denied. Only @xyz.vc accounts are allowed.",
      });
    }
  }, [searchParams]);

  const handleGoogleSignIn = async () => {
    setIsLoading(true);
    await signIn("google", { callbackUrl: "/" });
  };

  const handleTestSignIn = async () => {
    setIsLoading(true);
    await signIn("playwright", {
      email: "test@playwright.com",
      callbackUrl: "/",
    });
  };

  return (
    <div className="flex flex-col gap-3">
      <Button
        onClick={handleGoogleSignIn}
        disabled={isLoading}
        variant="outline"
        className="flex h-12 w-full items-center justify-center gap-3"
      >
        <GoogleIcon />
        <span>{isLoading ? "Signing in..." : "Continue with Google"}</span>
      </Button>

      {/* Test login button - only visible in Playwright test mode */}
      {isTestMode && (
        <Button
          onClick={handleTestSignIn}
          disabled={isLoading}
          variant="secondary"
          className="flex h-12 w-full items-center justify-center gap-3"
          data-testid="playwright-login"
        >
          <span>Sign in with Playwright (Test)</span>
        </Button>
      )}
    </div>
  );
}

export default function Page() {
  return (
    <div className="flex h-dvh w-screen items-center justify-center bg-background">
      <div className="flex w-full max-w-md flex-col gap-8 px-4">
        <div className="flex flex-col items-center gap-2 text-center">
          <h3 className="font-semibold text-xl dark:text-zinc-50">Sign In</h3>
          <p className="text-gray-500 text-sm dark:text-zinc-400">
            Sign in with your @xyz.vc Google account
          </p>
        </div>

        <Suspense
          fallback={
            <Button
              disabled
              variant="outline"
              className="flex h-12 w-full items-center justify-center gap-3"
            >
              <GoogleIcon />
              <span>Continue with Google</span>
            </Button>
          }
        >
          <LoginForm />
        </Suspense>
      </div>
    </div>
  );
}
